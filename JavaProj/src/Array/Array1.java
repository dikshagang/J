package Array;

public class Array1 {

	public static void main(String[] args) {
		int[] a= {10,20,89};
		
		System.out.println(a[2]);
		
		int[] b=new int[3];
		for(int bb:b) {
			System.out.println(bb);
		}
		
		int[] s=new int[4];
		for(int ss:s) {
			System.out.println(ss);
		}

		int[]d =new int[7];
		d[1]=20;
		d[2]=80;
		d[6]=90;
		for(int dd:d) {
			System.out.println(dd);
		}
		
	}

}
